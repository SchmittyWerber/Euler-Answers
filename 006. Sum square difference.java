package testerPack;

public class SumSqDiff {

	public static void main(String[] args) {
		long sum = 0;
		long sumSq = 0;
		long diff = 0;
		
		//computes the sum of all squares and the sum of all numbers
		//and the sum of all #s descending from the initial value of int i
		for (int i = 100; i > 0; i--) {
			sum += i * i;
			sumSq += i;
		}
		
		//computes the square of the sum of  i
		sumSq = sumSq * sumSq;
		diff = sumSq - sum;
		
		System.out.println(diff);
		
	}
}
